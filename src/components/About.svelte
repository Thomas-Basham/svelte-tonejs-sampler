  <h2 class="border-bottom">HOW TO USE</h2>
  <p>
    Play the samples by touch/click or drag one of the black time signature's
    over a sample to start a loop. Drag one of the reverbs over a sample to add
    reverb to that sample.
  </p>
  <p>Record your own samples or record and download your loop. Have fun!</p>
  <h2 class="border-bottom">ABOUT</h2>

  <p>
    Introducing the Svelte ToneJS Sampler app - a powerful and intuitive tool
    for creating and manipulating samples in real-time.
  </p>

  <p>
    Built using the Svelte JavaScript framework and the Tone.js library, this
    app offers a user-friendly interface for loading, layering, and sequencing
    samples. With its advanced features, such as reverb layering, time
    stretching, and filtering, users can easily create unique and dynamic
    soundscapes.
  </p>

  <p>
    In addition to its sample manipulation capabilities, the app also includes a
    built-in sequencer, loop recording, and support for recording your own
    sounds, making it a versatile option for both studio production and live
    performance.
  </p>

  <p>
    Whether you're a musician, producer, or just a hobbyist, the Svelte ToneJS
    Sampler app has everything you need to learn sample based music production.
